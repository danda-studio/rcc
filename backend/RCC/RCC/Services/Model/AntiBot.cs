namespace RCC.Services.Model
{
    /// <summary>
    /// Представляет метаданные антибот-защиты и информацию о верификации пользовательского взаимодействия,
    /// включая привязку к компании, время заполнения формы, оценку взаимодействия и проверку CAPTCHA.
    /// </summary>
    /// <remarks>
    /// Класс AntiBot обычно используется для сбора и передачи информации, которая помогает
    /// отличать легитимную активность пользователя от автоматизированных или вредоносных запросов.
    /// Чаще всего применяется в сценариях, где отправка форм или действия пользователя
    /// требуют защиты от автоматизированного злоупотребления, например с использованием CAPTCHA
    /// и поведенческого скоринга.
    /// </remarks>
    public class AntiBot
    {
        /// <summary>
        /// Получает или задаёт название компании, связанной с сущностью.
        /// </summary>
        public string? Company { get; set; }

        /// <summary>
        /// Получает или задаёт временную метку, указывающую момент начала заполнения формы.
        /// </summary>
        public long FormStart { get; set; }

        /// <summary>
        /// Получает или задаёт оценку взаимодействия пользователя.
        /// </summary>
        public int InteractionScore { get; set; }

        /// <summary>
        /// Получает или задаёт CAPTCHA-токен, переданный клиентом для верификации (Turnstile).
        /// </summary>
        /// <remarks>
        /// Это свойство обычно содержит токен ответа CAPTCHA-челленджа, например
        /// Cloudflare Turnstile. Токен используется для подтверждения того, что запрос
        /// был отправлен человеком, а не автоматизированным процессом.
        /// </remarks>
        public string? CaptchaToken { get; set; }
    }
}
